public class Grafica {
    String tipo;
    String nombre;
    
    public void Grafica(){
        tipo="";
        nombre="";
    
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
