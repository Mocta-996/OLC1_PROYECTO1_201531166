public class Lineas {
    
    String titulo ;
    String archivo ;
    public void Barras()
    {
        titulo = "";
        archivo = "";
       
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getArchivo() {
        return archivo;
    }

    public void setArchivo(String archivo) {
        this.archivo = archivo;
    }
    
}
